
# 需求 ： 实现简易版的计算器，要求能进行加减乘除

# 1. 函数的方式

# 加法
def add(x,y):
    return x + y

# 减法
def min(x,y):
    return x - y

# 乘法
def mul(x,y):
    return x * y

# 除法
def div(x,y):
    return x / y


# 2. 类的方式定义
